// Example Using a for loop to print numbers 1 to 10
for (let i = 1; i <= 10; i++) {
    console.log(`Number: ${i}`);
  }
  
  // Using a while loop to print a list of hobbies
  let color = [
    "yellow", 
    "blue",
    "green",
    "red",
    "purple",
    "orange",
    "black",
    "white",
    "pink",
    "brown" 
  ];
  
  let index = 0;
  while (index < color.length) {
    console.log(`Color: ${color[index]}`);
    index++;
  }